names="Sarthak Shukla"


# To slice the string (index:length we are slicing)
print(names[0:5])

# Automaticlly sets 0 index ([0:5])
print(names[:5])

# To get Length of the String
print(len(names))


# Negative Slicing
# python consider it as [0: len(names)-2]
print(names[0:-2])

# We can also start negiative for staring index tooo
str="Harry"
print(str[-4:-2])

